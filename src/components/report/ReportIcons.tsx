interface ReportIconProps {
  name: string;
  size?: number;
  className?: string;
}

export default function ReportIcon({ name, size = 20, className = "" }: ReportIconProps) {
  const path = ICON_PATHS[name];
  if (!path) return null;

  // gear icon has two path segments separated by " M"
  const segments = path.split(/(?= M)/);

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={1.5}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
    >
      {segments.map((d, i) => (
        <path key={i} d={d.trim()} />
      ))}
    </svg>
  );
}

export function getIconForSection(title: string): string {
  return SECTION_ICON_MAP[title] || "document";
}
